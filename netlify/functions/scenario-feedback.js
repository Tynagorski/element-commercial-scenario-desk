// Netlify Function: scenario-feedback
// Takes computed commercial-deal metrics/flags from the Scenario Desk and
// asks Claude for a short, professional broker-voice narrative assessment.
// The client already has a deterministic rule-based fallback, so this
// function can fail closed — any error just means the client uses that.

const Anthropic = require('@anthropic-ai/sdk');

const SYSTEM_PROMPT = `You are writing a short "Our Take" paragraph for a commercial real estate loan scenario, on behalf of Tyler Nagorski, Senior Mortgage Consultant at Element Commercial (NMLS #2346195), for the Element Commercial Scenario Desk tool.

You are given the deal inputs, computed metrics (LTV, DSCR, cash needed, etc.), a list of flags already generated by a rules engine, and an overall determination. Do not re-derive numbers — just interpret what's already been computed.

Write 2-4 sentences, in a direct, confident, broker-to-borrower voice (first person plural "we" is fine). Requirements:
- Reference the strongest 1-2 concerns first if there are any red or yellow flags, in plain English (not jargon-only)
- If the scenario is clean (mostly green flags), say so plainly and encourage sending documents
- Never promise approval, a rate, or specific terms — this is a preliminary screen only
- Do not invent facts not present in the provided data
- No headers, no bullet points, no markdown — plain prose only
- Keep it under 90 words`;

exports.handler = async (event) => {
  if (event.httpMethod !== 'POST') {
    return { statusCode: 405, body: 'Method Not Allowed' };
  }

  if (!process.env.ANTHROPIC_API_KEY) {
    return { statusCode: 500, body: JSON.stringify({ error: 'API key not configured' }) };
  }

  let payload;
  try {
    payload = JSON.parse(event.body);
    if (!payload || typeof payload !== 'object') throw new Error('invalid');
  } catch {
    return { statusCode: 400, body: JSON.stringify({ error: 'Bad request' }) };
  }

  const { inputs, metrics, flags, determination } = payload;

  try {
    const client = new Anthropic({ apiKey: process.env.ANTHROPIC_API_KEY });
    const response = await client.messages.create({
      model: 'claude-haiku-4-5-20251001',
      max_tokens: 220,
      system: SYSTEM_PROMPT,
      messages: [{
        role: 'user',
        content: JSON.stringify({ inputs, metrics, flags, determination })
      }]
    });

    return {
      statusCode: 200,
      headers: { 'Content-Type': 'application/json' },
      body: JSON.stringify({ narrative: response.content[0].text.trim() })
    };
  } catch (err) {
    console.error('Claude API error:', err);
    return { statusCode: 500, body: JSON.stringify({ error: 'Something went wrong' }) };
  }
};
